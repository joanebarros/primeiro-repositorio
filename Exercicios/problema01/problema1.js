// 1. Doado o tamanho da base e da altura de um triangulo, calcule a sua area e o seu perimetro.


let alturaRet = Number (prompt("Digite qual è a altura do retangulo?"))
let baseRet = Number (prompt("Digite qual è a base do retangulo?")) 

let areaRet = alturaRet*baseRet
let perimetroRet = alturaRet*2 + baseRet*2 

alert("A area do retangulo è: " + areaRet)
alert("O perimetro do retangulo è:" + perimetroRet)

